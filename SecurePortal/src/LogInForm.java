    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String t=LocalTime.now()+"";
        String email=tEmail.getText();
        String enroll=email;
        String password=tPassword.getText();
        try{
            if(DataManager.isPresent(enroll, email, password)){
                JOptionPane.showMessageDialog(null, "Logged In!");
            }else{
                JOptionPane.showMessageDialog(null,"Wrong details Entered");
                return;
            }
        }catch(SQLException e){JOptionPane.showMessageDialog(null,"Error: 404, TRY AGAIN");}
        DataManager.addLogInTime(email, t);
        new LogOutForm(email,LocalTime.now()).setVisible(true);
        this.setVisible(false);
        this.dispose();
        
    }//GEN-LAST:event_jButton1ActionPerformed

